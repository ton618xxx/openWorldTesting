using UnityEngine;
using UnityEngine.InputSystem;//библиотека для работы с новыми Input System

[DisallowMultipleComponent] //  Это страховка: иметь два CameraTargetController на одном объекте бессмысленно и привело бы к багам
[DefaultExecutionOrder(-100)] // -100 нам нужно, потому что Cinemachine читает позицию/поворот CameraTarget в каждом кадре. Если наш скрипт обновит поворот до Cinemachine, камера окажется в правильном месте. Если позже — на 1 кадр будет рассинхрон и возможна лёгкая дрожь. 
public class CameraTargetController : MonoBehaviour
{
    [Header("Mouse Sensitivity")]
    [SerializeField] private float sensitivityX = 0.15f; // Чувствительность по горизонтали. Обычно по горизонтали чувствительность чуть выше, чтобы было удобнее быстро поворачиваться.
    [SerializeField] private float sensitivityY = 0.12f; // Обычно по вертикали чувствительность чуть меньше, чтобы было удобнее целиться.
    [SerializeField] private bool invertY = false; //  Если true, то движение мыши вверх будет увеличивать угол поворота по X (поднимать камеру), а движение вниз — уменьшать (опускать камеру). Если false, то наоборот: движение вверх будет опускать камеру, а вниз — поднимать. Это просто вопрос предпочтений игрока.

    [Header("Vertical Clamp (Pitch)")]
    [SerializeField] private float minPitch = -35f;// ограничение угла поворота камеры вниз. Обычно не стоит позволять камере смотреть прямо вниз, чтобы не было неприятного ощущения "крутого виража". -35 градусов — это примерно оптимальный угол для большинства игр от третьего лица.
    [SerializeField] private float maxPitch = 70f; // ограничение угла поворота камеры вверх. Обычно стоит позволять камере смотреть выше горизонта, чтобы было удобно целиться по воздуху, но не слишком высоко, чтобы не было ощущения "крутого виража". 70 градусов — это примерно оптимальный угол для большинства игр от третьего лица.

    [Header("Cursor")]
    [SerializeField] private bool lockCursorOnStart = true; // флаг, который определяет, будет ли курсор мыши заблокирован и скрыт при запуске игры. Обычно для игр от третьего лица это желательно, чтобы курсор не отвлекал игрока и не мешал управлению камерой.

    private float yaw; // угол поворота камеры по горизонтали (вокруг оси Y).
    private float pitch; // угол поворота камеры по вертикали (вокруг оси X).
    private Transform myTransform;


    private void Awake()
    {
        myTransform = transform;
    }

    private void Start()
    {
        Vector3 e = myTransform.eulerAngles; 

        yaw = e.y; 
        pitch = NormalizePitch(e.x); 

        if (lockCursorOnStart)
        {
            Cursor.lockState = CursorLockMode.Locked; 
            Cursor.visible = false; 
        }
    }

    private void Update()
    {
        var mouse = Mouse.current;
        if (mouse == null) return; 

        Vector2 delta = mouse.delta.ReadValue();
        if (delta.sqrMagnitude < 0.0001f) return; 

        yaw += delta.x * sensitivityX; 
        pitch += (invertY ? 1f : -1f) * delta.y * sensitivityY; 
        pitch = Mathf.Clamp(pitch, minPitch, maxPitch); 
    }

    private void LateUpdate()
    {
        // Применяем поворот здесь. Это гарантирует, что Cinemachine увидит правильный поворот 
        // ПОСЛЕ того, как персонаж переместился и повернулся в Update. 
        // Это предотвращает "эффект спирали" или "spinning".
        myTransform.rotation = Quaternion.Euler(pitch, yaw, 0f); 
    }

    private static float NormalizePitch(float angle)// принимаем сюда угол в градусах, который может быть в диапазоне от 0 до 360, и преобразуем его в диапазон от -180 до 180 градусов. Это нужно, чтобы было удобнее работать с углами поворота по вертикали (pitch) и применять ограничения. Например, если угол равен 350 градусов, то он эквивалентен -10 градусам, что означает, что камера смотрит немного вниз. Если угол равен 30 градусов, то он остается 30 градусами, что означает, что камера смотрит немного вверх. Если угол равен 180 градусов, то он остается 180 градусами, что означает, что камера смотрит прямо вперед. Если угол равен 270 градусов, то он эквивалентен -90 градусам, что означает, что камера смотрит прямо вниз.
    {
        if (angle > 180f) angle -= 360f;// Если угол больше 180 градусов, то вычитаем 360 градусов, чтобы получить эквивалентный угол в диапазоне от -180 до 180 градусов. Например, если угол равен 350 градусов, то после вычитания 360 градусов он станет -10 градусами, что означает, что камера смотрит немного вниз.
        return angle;// Возвращаем нормализованный угол, который теперь находится в диапазоне от -180 до 180 градусов. Например, если входной угол был 350 градусов, то выходной угол будет -10 градусами. Если входной угол был 30 градусов, то выходной угол будет 30 градусами. Если входной угол был 180 градусов, то выходной угол будет 180 градусами. Если входной угол был 270 градусов, то выходной угол будет -90 градусами.
    }
}
